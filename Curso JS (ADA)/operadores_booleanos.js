/* [Operadores Booleanos] */

/*
Igualdade:                == (ou ===) <-- (três iguais) só é mostrado se for do mesmo tipo
Desigualdade/Diferente:   != (ou !==) <-- mesma coisa acima
Maior que:                >
Maior ou igual que:       >=
Menor que:                <
Menor ou igual que:       <=
*/

let numero = 10;

console.log("Seu numero é: ", numero);
console.log("Seu é ao igual 21 ? ", numero == 21);
console.log("Seu é ao diferente 21 ? ", numero != 21);
console.log("Seu é ao maior que 21 ? ", numero > 21);
console.log("Seu é ao maior ou igual que 21 ? ", numero >= 21);
console.log("Seu é ao menor que 21 ? ", numero < 21);
console.log("Seu é ao menor ou igual que 21 ? ", numero <= 21);

