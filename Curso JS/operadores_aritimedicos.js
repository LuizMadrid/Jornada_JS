/* [Operadores aritimeticos] */

let numero1 = 20;
let numero2 = 2;

/* 
Adição/Concatenação:                +
Substração:                         -
Multiplicação:                      *
Divisão:                            /
Exponenciação:                     **
Resto de Divisão:                   % 
*/

console.log("Sua soma deu: ", numero1 + numero2);
console.log("Sua subtração deu: ", numero1 - numero2);
console.log("Sua multiplicação deu: ", numero1 * numero2);
console.log("Sua divisão deu: ", numero1 / numero2);
console.log("Sua Exponenciação deu: ", numero1 ** numero2);
console.log("Seu resto de divisão deu: ", numero1 % numero2);
